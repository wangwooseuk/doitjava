package example;

/*
 * 숫자 10부터 1까지 거꾸로 출력하는
 * 프로그램을 작성하세요.
 */

public class Exam09 {

	public static void main(String[] args) {
		for(int i=0; i<=10; i++) {
			System.out.println(i);
		}
		System.out.println();
		
		for(int i=10; i>=1; i--) {
			System.out.println(i);
		}
	}
}
