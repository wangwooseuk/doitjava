package chapter02;

public class BinaryTest2 {

	public static void main(String[] args) {
		int bNum = 0B10100;	//2진수
		int dNum = 20;		//10진수
		int oNum = 024;		//8진수
		int hNum = 0x14;	//16진수
		
		System.out.println(bNum);
		System.out.println(dNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}
