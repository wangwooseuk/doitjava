package chapter02;

public class CharEx2 {

	public static void main(String[] args) {
		char ch1 = 'B';
		System.out.println(ch1);
		
		int iNum = ch1;
		System.out.println(iNum);
		
		char ch2 = 'D';
		System.out.println(ch2);
		
		int iNum2 = ch2;
		System.out.println(iNum2);
		
		char ch5 = 'b';
		int iNum4 = ch5;
		System.out.println(iNum4);
		System.out.println('b' - 'B');
	}

}
