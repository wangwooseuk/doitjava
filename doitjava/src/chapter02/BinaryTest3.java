package chapter02;

public class BinaryTest3 {

	public static void main(String[] args) {
		int bNum = 0B1111; //2진수
		int dNum = 15; //10진수
		int oNum = 017; //8진수
		int hNum = 0xf; //16진수
		System.out.println(bNum);
		System.out.println(dNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}
